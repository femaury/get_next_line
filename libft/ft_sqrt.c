#include "libft.h"

int		ft_sqrt(int nb)
{
	int		i;

	i = 10;
	while (i * i < nb)
		i += 10;
	i -= 10;
	while (i * i < nb)
		i++;
	return (i * i == nb) ? i : --i;
}
